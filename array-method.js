//Array Method- pop(), push(), shift(), unshift()
//pop()
var fruits = ['apple', 'banana0', 'mango'];
console.log(fruits.pop());
console.log(fruits);

//push()
fruits.push('lemon');
console.log(fruits);

//shift()
fruits.shift();
console.log(fruits);

//unshift()
fruits.unshift('black bary');
console.log(fruits);






